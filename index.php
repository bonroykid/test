            <h2>Máte problémy při tamponovém tisku?</h2>
            <p>
            Při tamponovém tisku sbírá tampon barvu z motivu vyleptaném na štočku a otiskuje ji na předmět. Tento přenos barvy není a nemůže být dokonalý. Na tamponu nám tedy zbyde při každém tisku trocha barvy.
            nečistoty
            </p>
            <p>
            I při sebevětší péči o čistoutu sbírá tampon z předmětu drobné nečistoty, které pak přenáší na štoček a&nbsp;odtud se dostávají až do barvy.
            </p>
            <p>
            Tyto nepříjemné jevy pak způsobují řadu těžko řešitelných potíží:
            </p>
            <p><strong>Prskání barvy:</strong> Je způsobeno příliš silnou vrstvou barvy.</p>
            <p><strong>Snižování kvality barvy:</strong> Nečistoty, které se ze štočku dostávají do barvy snižují její kvalitu a&nbsp;znehodnocují&nbsp;ji.</p>
            <p><strong>Poškrábání štočku:</strong> Mezi štoček a&nbsp;barevník se dostane drobná částečka nečistoty a škrábne štoček. V tisku se objeví tenká linka ve směru pohybu barevníku a nezbývá než vyrobit nový štoček.</p>
            <p><strong>Nižší životnost štočku:</strong> Nečistoty v&nbsp;barvě a zaschlé zbytky barvy způsobují rychlejší opotřebení štočku.</p>
